    // 遗物价值分(入路线打分): S=7 A=5 B=3.5 C=2 D=0.5; 未知遗物给 2(保守 C 档)
    public static double Value(string entry)
    {
        if (entry == null) return 2.0;
        if (!RelicTable.TryGetValue(entry, out var v)) return 2.0;
        return v.Tier.TrimEnd('?') switch { "S" => 7.0, "A" => 5.0, "B" => 3.5, "C" => 2.0, "D" => 0.5, _ => 2.0 };
    }

    public static string Tier(string entry) =>
        entry != null && RelicTable.TryGetValue(entry, out var v) ? v.Tier : "?";

    public static string Arch(string entry) =>
        entry != null && RelicTable.TryGetValue(entry, out var v) && v.Arch != "" ? v.Arch : null;
}
